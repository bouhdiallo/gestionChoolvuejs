

export function useSwalSuccess(message) {

    Swal.fire({
        toast:true,
title: "message",
icon: "success",
animation: false,
position: "top-end", 
showConfirmButton: false,
timer: 4000,
});
}



export function useSwalError(message) {

    Swal.fire({
        toast:true,
title: "message",
icon: "error",
animation: false,
position: top-end, 
showConfirmButton,
timer: 4000,
});
}









